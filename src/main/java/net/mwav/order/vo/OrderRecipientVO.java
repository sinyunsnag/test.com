package net.mwav.order.vo;

public class OrderRecipientVO {
	private int order_id;
	private String orName;
	private String orPhone;
	private String orCellularP;
	private String orZipcode;
	private String orAddress;
	
	public int getOrder_id() {
		return order_id;
	}
	public void setOrder_id(int order_id) {
		this.order_id = order_id;
	}
	public String getOrName() {
		return orName;
	}
	public void setOrName(String orName) {
		this.orName = orName;
	}
	public String getOrPhone() {
		return orPhone;
	}
	public void setOrPhone(String orPhone) {
		this.orPhone = orPhone;
	}
	public String getOrCellularP() {
		return orCellularP;
	}
	public void setOrCellularP(String orCellularP) {
		this.orCellularP = orCellularP;
	}
	public String getOrZipcode() {
		return orZipcode;
	}
	public void setOrZipcode(String orZipcode) {
		this.orZipcode = orZipcode;
	}
	public String getOrAddress() {
		return orAddress;
	}
	public void setOrAddress(String orAddress) {
		this.orAddress = orAddress;
	}
	
	

}
